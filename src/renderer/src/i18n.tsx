const translations = { ko, en }

interface I18nContextValue {
  language: AppLanguage
  languagePreference: LanguagePreference
  systemLanguage: AppLanguage
  setLanguagePreference: (preference: LanguagePreference) => void
  t: (key: TranslationKey, vars?: TranslationVars) => string
}

function isAppLanguage(value: string | null): value is AppLanguage {
  return value === 'ko' || value === 'en'
}

export function detectSystemLanguage(): AppLanguage {
  const candidates = typeof navigator !== 'undefined'
    ? [navigator.language, ...(navigator.languages ?? [])]
    : []
  return candidates.some(lang => lang?.toLowerCase().startsWith('ko')) ? 'ko' : 'en'
}

export function loadLanguagePreference(): LanguagePreference {
  try {
    const saved = localStorage.getItem(LANGUAGE_STORAGE_KEY)
    if (saved === 'system' || isAppLanguage(saved)) return saved
  } catch {
    // localStorage를 사용할 수 없으면 운영환경 언어를 사용합니다.
  }
  return 'system'
}

export function resolveLanguagePreference(preference: LanguagePreference, systemLanguage: AppLanguage): AppLanguage {
  return preference === 'system' ? systemLanguage : preference
}

export function translate(language: AppLanguage, key: TranslationKey, vars?: TranslationVars): string {
  const template = translations[language][key] ?? ko[key] ?? key
  if (!vars) return template
  return template.replace(/\{(\w+)\}/g, (_, name: string) => {
    const value = vars[name]
    return value === null || value === undefined ? '' : String(value)
  })
}

export function isTranslationKey(key: string): key is TranslationKey {
  return key in ko
}

const I18nContext = createContext<I18nContextValue | null>(null)

export function I18nProvider({ value, children }: { value: I18nContextValue; children: ReactNode }): JSX.Element {
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n(): I18nContextValue {
  const context = useContext(I18nContext)
  if (!context) throw new Error('useI18n must be used inside I18nProvider')
  return context
}
